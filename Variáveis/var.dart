void main(List<String> args) {
  //Variável sem tipagem.
  var name = 'Eder 1';
  print(name);

  //Variável com tipagem declarada.
  String sName = 'Eder 2';
  print(sName);

  //Variável do tipo Object ou dynamic.
  Object oName = 'Eder 321';
  print(oName);

  dynamic dName = 'Eder 123';
  print(dName);
}
